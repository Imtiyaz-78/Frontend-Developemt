//JavaScript String charAt() function:- Ek character nikalne ke liye use karte hai


var companyName = "microsoft";
var result = companyName.charAt(0);
console.log(result);

result = companyName.charAt(2);
console.log(result);

result = companyName.charAt(30);
console.log(result);

result = companyName.charAt(companyName.length-1);
console.log(result);

result = companyName.charAt(companyName.length-5);
console.log(result);

