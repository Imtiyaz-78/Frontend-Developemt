
// ---> slice
// slice() is method which extracts a part of a string and returns the extracted part in a new string.

// The method takes 2 parameters: the start position, and the end position (end not included).

// slice(start position, end position)
// node slice.js
var str = "Apple, Banana, Kiwi";
var result = str.slice(7, 13); // output ----> Banana
console.log(result);
