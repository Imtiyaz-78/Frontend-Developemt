// 1. JavaScript Array unshift()
// 2. The unshift() method adds a new element to an array (at the beginning), and "unshifts" older elements:


const list = ["Banana", "Orange", "Apple", "Mango"];
list.unshift("first");
console.log(list); /// ouput ---->  ["first", "Orange", "Apple", "Mango"];
