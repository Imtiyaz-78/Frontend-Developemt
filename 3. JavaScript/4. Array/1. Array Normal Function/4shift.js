// 1. JavaScript Array shift()
// 2. The shift() method removes the first element from the array
// 3. The shift() method returns the value that was "shifted out":


var list = [45,78,34,12,77];
var result = list.shift();
console.log("after removing value at the front position", result); // output --> 45
console.log("after removing value at the front position", list); //output-->  //  [78,34,12,77]
